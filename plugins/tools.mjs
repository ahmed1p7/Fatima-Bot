const activeDuas = {};

function isValidUrl(string) {
  try {
    const url = new URL(string);
    return url.protocol === 'http:' || url.protocol === 'https:';
  } catch {
    return false;
  }
}

export default {
  name: 'Tools',
  commands: ['دعاء', 'dua', 'إيقاف_دعاء', 'stopdua', 'اختصار', 'short'],
  
  async execute(sock, msg, ctx) {
    const { from, command, args, text, prefix, isGroupAdmin, isOwner } = ctx;
    
    if (['دعاء', 'dua'].includes(command)) {
      if (!isGroupAdmin && !isOwner) {
        return sock.sendMessage(from, { text: '❌ للمشرفين فقط!' });
      }
      
      const mins = parseInt(args[0]);
      if (!mins) {
        return sock.sendMessage(from, { 
          text: `🤲 أدعية متنوعة:\n\n${DUAS.slice(0, 5).join('\n')}\n...وغيرها\n\n💡 ${prefix}دعاء 30\n⚠️ الحد الأدنى 5 دقائق، الحد الأقصى 60 دقيقة` 
        });
      }
      
      if (mins < 5 || mins > 60) {
        return sock.sendMessage(from, { text: '❌ أدخل وقتًا بين 5 و60 دقيقة!' });
      }
      
      if (activeDuas[from]) {
        clearInterval(activeDuas[from].int);
        delete activeDuas[from];
      }
      
      const dua = DUAS[Math.floor(Math.random() * DUAS.length)];
      const interval = setInterval(() => {
        sock.sendMessage(from, { 
          text: DUAS[Math.floor(Math.random() * DUAS.length)] + `\n📢 كل ${mins} دقيقة` 
        }).catch(() => {
          if (activeDuas[from]) {
            clearInterval(activeDuas[from].int);
            delete activeDuas[from];
          }
        });
      }, mins * 60000);
      
      activeDuas[from] = { int: interval, start: Date.now(), mins };
      
      return sock.sendMessage(from, { 
        text: `${dua}\n\n✅ سيتم الإرسال كل ${mins} دقيقة\n🛑 ${prefix}إيقاف_دعاء` 
      });
    }
    
    if (['إيقاف_دعاء', 'stopdua'].includes(command)) {
      if (!activeDuas[from]) {
        return sock.sendMessage(from, { text: '❌ لا يوجد تكرار نشط!' });
      }
      clearInterval(activeDuas[from].int);
      delete activeDuas[from];
      return sock.sendMessage(from, { text: '✅ تم إيقاف التكرار!' });
    }
    
    if (['اختصار', 'short'].includes(command)) {
      if (!text) {
        return sock.sendMessage(from, { text: `❌ اكتب الرابط!\n💡 ${prefix}اختصار https://example.com` });
      }
      
      if (!isValidUrl(text)) {
        return sock.sendMessage(from, { text: '❌ الرابط غير صالح! يجب أن يبدأ بـ http:// أو https://' });
      }
      
      try {
        const response = await fetch(`https://tinyurl.com/api-create.php?url=${encodeURIComponent(text)}`);
        const shortUrl = await response.text();
        if (!shortUrl || shortUrl.includes('error')) {
          throw new Error('فشل الاختصار');
        }
        return sock.sendMessage(from, { text: `✅ تم الاختصار:\n\n🔗 ${shortUrl}` });
      } catch (error) {
        return sock.sendMessage(from, { text: '❌ فشل الاختصار! حاول مرة أخرى.' });
      }
    }
  }
};
